'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''

dist_miles = 10
dist_km = dist_miles * 1.6
time_mins = 30.5
time_hrs = time_mins / 60
av_speed_kph = dist_km / time_hrs
print('average speed = ', str(av_speed_kph), 'kilometres per hour')
# av_speed_kph = 31.475 kph

